import { Fragment } from 'react';
import { BrowserRouter as Router, Routes, Route } from 'react-router-dom';
import { publicRoutes, privateRoutes } from '~/routes';
import { DefaultLayout } from '~/components/Layout';

function App() {
    return (
        <Router>
            <div className="App">
                <Routes>
                    {publicRoutes.map((route, index) => {
                        // if page has no layout => wrap by Fragment tag
                        // else => wrap by the layout
                        // if there's no layout specified, wrap by default layout
                        let Layout = {};
                        if (route.layout === null) {
                            Layout = Fragment;
                        } else if (route.layout === undefined) {
                            Layout = DefaultLayout;
                        } else {
                            Layout = route.layout;
                        }
                        const Page = route.component;
                        return (
                            <Route
                                key={index}
                                path={route.path}
                                element={
                                    <Layout>
                                        <Page />
                                    </Layout>
                                }
                            />
                        );
                    })}
                </Routes>
            </div>
        </Router>
    );
}

export default App;
